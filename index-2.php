								<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;据悉，本届葡萄节采取了摘葡萄与玩漂流、赏古镇、游乡村相结合的新模式，游客既可上四明山体验漂流的乐趣，也可欣赏临山卫古建筑、古遗址，感受“卫城文化”的博大精深和幸福临山的文化魅力，还能充分领略临山镇南片刺绕湾休闲渔业、中部味香园葡萄采摘观光、北部杭州湾观潮等自然景观。</p>
