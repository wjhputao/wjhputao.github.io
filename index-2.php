								<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;7月22日，一批来自杭州的游客冒着酷暑，走进余姚临山镇味香园绿色葡萄休闲观光长廊，一边吃着亲手采摘的葡萄，一边体验田园风情，喜滋滋地过了一把农家瘾。当天，2014余姚•临山江南葡萄节在味香园专业合作社拉开大幕。活动将持续到9月10日。</p>
